package Operator;
//자바의 정석 예제 코드
public class OperatorEx18 {
    public static void main(String[] args) {
        int x = 10;
        int y = 8;
        // %는 나머지 연산을 구할 떄 사용한다. 
        // 짝수, 홀수, 배수검사에 사용된다.
        System.out.printf("%d 을 %d 로 나누면 %n,",x,y);
        System.out.printf("나누기는 %d이고 나머지는 %d 입니다.",x/y,x%y);


    }
}
