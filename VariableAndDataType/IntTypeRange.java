package VariableAndDataType;
public class IntTypeRange{
    public static void main() {
        // byte (-128) ~ 127
        byte b1 = -127;
        byte b2 = -129;
        byte b3 = 127;
        byte b4 = 128;

        // short (-32,768) ~ 32,767 ||(-32768) ~ 32767
        short s1 = -32768;
        short s2 = -32769;
        short s3 = 32768;
        short s4 = 32767;

        // int (-2,147,483,648) ~ 2,147,483,647 ||(-2147483648) ~ 2147483647
        int i1 = -2147483648;
        int i2 = -2147483649;
        int i3 = 2147483647;
        int i4 = 2147483648;

        // long (-9,223,372,036,854,775,808) ~ 9,223,372,036,854,775,807 ||
        // (-9223372036854775808) ~ 922337203685477,807
        long l1 = -9223372036854775800;
        long l2 =  -9223372036854775808;
        long l3 = 9223372036854775807;
        long l4 = 9223372036854775808;



    }
}